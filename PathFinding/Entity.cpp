#include "Entity.h"
#include "Engine.h"

// ---------------------------------------------------------------------------------------------------------------------
// Inicializaçăo de membros estáticos da classe

sf::RenderWindow*&	 Entity::window{ Engine::window }; ///< Janela gráfica
sf::Event&			 Entity::event{ Engine::event };   ///< Eventos da janela
std::vector<Entity*> Entity::objects{};				   ///< Vetor de objetos

// ---------------------------------------------------------------------------------------------------------------------

Entity::Entity()
{
	Register(this);
}

// ---------------------------------------------------------------------------------------------------------------------

Entity::~Entity()
{
	Unregister(this);
}

// ---------------------------------------------------------------------------------------------------------------------

void Entity::Register(Entity* obj)
{
	objects.push_back(obj);
}

// ---------------------------------------------------------------------------------------------------------------------

void Entity::Unregister(Entity* obj)
{
	objects.erase(std::remove(objects.begin(), objects.end(), obj), objects.end());
}

// ---------------------------------------------------------------------------------------------------------------------

void Entity::UpdateObjects()
{
	for (Entity* obj : objects)
		obj->Update();
}

